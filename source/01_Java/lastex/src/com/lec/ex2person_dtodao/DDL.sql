SELECT ROWNUM RN, A.* 
        FROM (SELECT  pNAME || '(' || pNO || '번)' "pNAME", jNAME, KOR, ENG, MAT, KOR+ENG+MAT "TOT" 
            FROM PERSON P, JOB J
            WHERE P.jNO=J.jNO AND jNAME='배우' ORDER BY TOT DESC) A
        WHERE jNAME = '배우';


