-- 더미데이터 생성

DROP TABLE PERSON;
DROP TABLE JOB;
DROP SEQUENCE PERSON_NO_SQ;


CREATE TABLE JOB (
    jNO NUMBER(2,0),
    jNAME VARCHAR2(50) NOT NULL,
    PRIMARY KEY (jNO)
);

CREATE TABLE PERSON (
    pNO NUMBER(5),
    pNAME VARCHAR2(50) NOT NULL,
    jNO NUMBER(2,0) NOT NULL,
    KOR NUMBER(3) CHECK (KOR>=0),
    ENG NUMBER(3) CHECK (ENG>=0),
    MAT NUMBER(3) CHECK (MAT>=0),
    PRIMARY KEY (pNO),
    FOREIGN KEY (jNO) REFERENCES JOB(jNO)
);

CREATE SEQUENCE PERSON_NO_SQ
    START WITH 1
    MAXVALUE 999
    NOCYCLE
    NOCACHE;

