package com.lec.ex;
//기본 데이터 타입 (기초 데이터 타입);소문자  vs. 참조 데이터 타입 (객체 데이터 타입);대문자
public class VarEx05 {
	public static void main(String[] args) {
		// 기본|기초 데이터 타입 (소문자로 시작)
		int i;  //4byte
		i = 10;
		// 참조|객체 데이터 타입 (대문자로 시작) 참조데이터 타입은 할당된 값의 주소만 불러옴 (주소를 레퍼런스/참조한다)
		String name;
		name = "홍길동";
	}
}
